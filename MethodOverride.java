
public class MethodOverride 
{
	public static void main(int a) 
	{
		int b= a+10;
		System.out.println(b);

	}
	public static void main(float a) 
	{
		float b= a+12;
		System.out.println(b);

	}

	public static void main(String[] args) 
	{
		float a=12;
		main(a);
		

	}

}
